type question36 struct {
	para36
	ans36
}

// para 是参数
// one 代表第一个参数
type para36 struct {
	s [][]byte
}

// ans 是答案
// one 代表第一个答案
type ans36 struct {
	s bool
}

func onlyValidChars(board [][]byte) bool {
	for _, row := range board {
		for _, c := range row {
			if c != '.' && (c < '1' || c > '9') {
				return false
			}
		}
	}
	return true
}

